#include <iostream>

// funkcja mnożenia 2 liczb

double mnożenie(double a, double b) {
    return a * b;
}

// Funkcja dzielenia dwóch liczb
double dzielenie(double a, double b) {
    if (b != 0) {
        return a / b;
    } else {
        std::cout << "Błąd: Nie można dzielić przez zero!" << std::endl;
        return 0;
    }
}
